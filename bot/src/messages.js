'use strict';

/**
 * כל טקסטי הבוט בעברית — טון מקצועי וחם.
 * מרוכז כאן כדי שיהיה קל לערוך ניסוחים בלי לגעת בלוגיקה.
 */

const { formatPrice, PACKAGE_INCLUDES, TEAM_PER_PERSON } = require('./config/pricing');

// הצעה לעבור לשיחה אישית — נספחת בכל שלב בשיחה.
const HANDOFF_HINT =
  'בכל שלב תוכלו לדבר ישירות עם שרון — כתבו *שרון* או *טלפון* ואחזיר אתכם לשיחה אישית. 📞';

const messages = {
  handoffHint: HANDOFF_HINT,

  greeting() {
    return (
      'שלום וברוכים הבאים לסטודיו הצילום של שרון גבאי 📸✨\n' +
      'אני העוזר/ת הדיגיטלי/ת של שרון, ואשמח לעזור לכם לתאם צילום תדמית או פורטרט (פריסה ארצית).\n\n' +
      'נתחיל בכמה שאלות קצרות כדי להתאים לכם בדיוק את מה שצריך.\n\n' +
      'מה סוג הצילום שאתם מחפשים?\n' +
      '1️⃣ פורטרט אישי / תדמית\n' +
      '2️⃣ הדשוטס לעובד או לכמה אנשים בודדים\n' +
      '3️⃣ צילום צוות / קבוצה\n' +
      '4️⃣ חברה גדולה — הדשוטס לעובדים רבים\n\n' +
      'אפשר לבחור מספר, או פשוט לכתוב במילים שלכם.\n' +
      HANDOFF_HINT
    );
  },

  askWhen() {
    return (
      'נהדר 🙏 מתי בערך תרצו לצלם?\n' +
      'למשל: "בשבועיים הקרובים", "בחודש הבא", "אין לי לחץ".\n' +
      HANDOFF_HINT
    );
  },

  askWhere() {
    return (
      'מעולה. איפה יהיה נוח לכם לצלם?\n' +
      '1️⃣ בסטודיו של שרון\n' +
      '2️⃣ אצל הלקוח (שרון מגיעה אליכם ומקימה סטודיו מלא בשטח — תוצאה זהה לסטודיו)\n\n' +
      HANDOFF_HINT
    );
  },

  notUnderstood() {
    return (
      'סליחה, לא הצלחתי להבין 🙂 אפשר לבחור מספר מהאפשרויות, או לכתוב שוב במילים אחרות.\n' +
      HANDOFF_HINT
    );
  },

  // הצגת חבילות עבור מיקום (studio/onsite).
  presentPackages(location) {
    const { PACKAGES } = require('./config/pricing');
    const loc = PACKAGES[location];
    const t = loc.tiers;

    const includes = PACKAGE_INCLUDES.map((x) => `   • ${x}`).join('\n');

    return (
      `הנה החבילות שלנו ${loc.label}:\n\n` +
      `1️⃣ *בסיס* — ${formatPrice(t.base.price)}\n` +
      `   ${t.base.photos} תמונות, ${t.base.sets} סטים\n\n` +
      `2️⃣ *סטנדרט* — ${formatPrice(t.standard.price)}  ⭐ *הכי מומלצת*\n` +
      `   ${t.standard.photos} תמונות, ${t.standard.sets} סטים\n` +
      `   החבילה שרוב הלקוחות בוחרים — האיזון הטוב ביותר בין מגוון לתמורה.\n\n` +
      `3️⃣ *פרימיום* — ${formatPrice(t.premium.price)}\n` +
      `   ${t.premium.photos} תמונות, ${t.premium.sets} סטים\n\n` +
      `כל החבילות כוללות:\n${includes}\n\n` +
      'איזו חבילה מתאימה לכם? (אפשר לבחור מספר, או 1/2/3)\n' +
      HANDOFF_HINT
    );
  },

  teamPricing() {
    return (
      'לצילום צוות / קבוצה התמחור הוא לפי מספר המשתתפים — ' +
      `${formatPrice(TEAM_PER_PERSON.min)}–${formatPrice(TEAM_PER_PERSON.max)} לאדם.\n` +
      'כל משתתף מקבל צילום מקצועי, וההצעה המדויקת נסגרת לפי מספר העובדים והצרכים.\n\n' +
      'נשמח לתאם עם שרון שיחה קצרה כדי לבנות לכם הצעה מדויקת. רוצים שאחבר אתכם? (כן/לא)\n' +
      HANDOFF_HINT
    );
  },

  bigCompany() {
    return (
      'איזה כיף, פרויקט הדשוטס לעובדים רבים זה בדיוק הסוג של דברים ששרון אוהבת 🙌\n' +
      'בפרויקטים בהיקף כזה אנחנו לא קובעים מועד אוטומטית — שרון מתאמת אתכם ישירות ' +
      'כדי לבנות לוח זמנים שמתאים למספר העובדים ולמקום.\n\n' +
      'אעביר את הפנייה לשרון עכשיו, והיא תחזור אליכם לתיאום שיחה. ' +
      'על שם מי לרשום, ובאיזה מספר ליצור קשר?'
    );
  },

  // הצגת מועדי סטודיו פנויים.
  presentStudioSlots(slots, tierLabel) {
    if (!slots.length) {
      return (
        'כרגע לא מצאתי משבצת פנויה קרובה ביומן 😕 ' +
        'אעביר את הפנייה לשרון והיא תתאם אתכם מועד באופן אישי.'
      );
    }
    const list = slots
      .map((s, i) => `${i + 1}️⃣ ${s.dateLabel}, בשעה ${s.startLabel}–${s.endLabel}`)
      .join('\n');
    return (
      `מצוין, נתאם מועד לצילום ${tierLabel} 🗓️\n` +
      'אלה המועדים הפנויים הקרובים (ימים א׳–ה׳, בין 9:30 ל-13:30):\n\n' +
      list +
      '\n\nאיזה מהם מתאים לכם? (בחרו מספר)\n' +
      HANDOFF_HINT
    );
  },

  // הצגת ימים פנויים לצילום אצל הלקוח.
  presentOnsiteDays(days) {
    if (!days.length) {
      return (
        'כרגע לא מצאתי יום פנוי לחלוטין קרוב ביומן 😕 ' +
        'אעביר את הפנייה לשרון והיא תתאם אתכם מועד באופן אישי.'
      );
    }
    const list = days.map((d, i) => `${i + 1}️⃣ ${d.dateLabel}`).join('\n');
    return (
      'צילום אצל הלקוח שומר לכם יום שלם ביומן של שרון 🗓️\n' +
      'את שאר הפרטים (שעת הגעה, חניה, חדר וכו׳) שרון תסגור אתכם בנפרד.\n\n' +
      'אלה הימים הפנויים הקרובים (ימים א׳–ה׳):\n\n' +
      list +
      '\n\nאיזה יום מתאים לכם? (בחרו מספר)\n' +
      HANDOFF_HINT
    );
  },

  askName() {
    return 'כמעט סיימנו 🙂 על שם מי לרשום את הפנייה?';
  },

  askPhone() {
    return 'ומה מספר הטלפון / וואטסאפ הכי נוח ליצירת קשר?';
  },

  confirmStudio(name, tier, booking) {
    return (
      `מעולה, ${name}! קבעתי לכם 🎉\n\n` +
      `📅 ${booking.dateLabel}, בשעה ${booking.startLabel}–${booking.endLabel}\n` +
      `📸 חבילת ${tier.label} — ${formatPrice(tier.price)} (${tier.photos} תמונות, ${tier.sets} סטים)\n\n` +
      'שרון קיבלה את כל הפרטים ותחזור אליכם עם אישור ושיחת אפיון קצרה לפני הצילום.\n' +
      'מחכים לראות אתכם! 😊'
    );
  },

  confirmOnsite(name, tier, booking) {
    return (
      `מעולה, ${name}! שמרתי לכם את היום 🎉\n\n` +
      `📅 ${booking.dateLabel} — יום מלא שמור עבורכם\n` +
      `📸 חבילת ${tier.label} אצל הלקוח — ${formatPrice(tier.price)} (${tier.photos} תמונות, ${tier.sets} סטים)\n\n` +
      'שרון קיבלה את כל הפרטים, תיצור אתכם קשר לסגירת שעת ההגעה ושאר הפרטים, ' +
      'ותקיים שיחת אפיון קצרה לפני הצילום.\n' +
      'מחכים לראות אתכם! 😊'
    );
  },

  handoff(name) {
    const who = name ? `${name}, ` : '';
    return (
      `${who}בשמחה 🙏 אעביר את הפרטים לשרון והיא תיצור אתכם קשר באופן אישי בהקדם.\n` +
      'אם תרצו, אפשר גם להשאיר כאן עוד פרטים והם יצורפו לפנייה.'
    );
  },

  askContactForHandoff() {
    return 'כדי ששרון תוכל לחזור אליכם — על שם מי לרשום, ובאיזה מספר ליצור קשר?';
  },

  goodbye() {
    return 'תודה רבה ויום נפלא! 🌟 אם תצוץ עוד שאלה, אני כאן.';
  },
};

module.exports = messages;
