'use strict';

/**
 * הדגמה אוטומטית — מריצה כמה תרחישי שיחה מלאים בלי הקלדה,
 * כדי לראות בקלות שהבוט שואל נכון, מתמחר נכון, ומדמה קביעת מועד נכון.
 * הרצה:  npm run demo   (או: node src/demo.js)
 */

const { Brain } = require('./brain');
const { MockCalendar } = require('./calendar');
const { notifySharon } = require('./integrations/notify');

// יום בסיס קבוע כדי שההדגמה תהיה זהה בכל הרצה (ראשון, 21/06/2026).
const BASE_DATE = new Date('2026-06-21T00:00:00');

function runScenario(title, userInputs) {
  console.log('\n\n##################################################');
  console.log('# תרחיש: ' + title);
  console.log('##################################################');

  const calendar = new MockCalendar(BASE_DATE);
  const brain = new Brain({ calendar, notify: notifySharon });

  const show = (lines) => lines.forEach((l) => console.log('\n🤖 ' + l + '\n'));

  show(brain.start());
  for (const input of userInputs) {
    console.log('🧑 ' + input);
    show(brain.receive(input));
  }
}

// 1) צילום בסטודיו, חבילת סטנדרט — קביעת מועד מלאה.
runScenario('סטודיו · סטנדרט · קביעת מועד', [
  '1', // פורטרט / תדמית
  'בשבועיים הקרובים',
  '1', // בסטודיו
  '2', // סטנדרט
  '1', // המועד הראשון המוצע
  'דנה כהן',
  '052-1234567',
]);

// 2) צילום אצל הלקוח, חבילת פרימיום — חוסם יום שלם.
runScenario('אצל הלקוח · פרימיום · יום מלא', [
  'אני רוצה צילום תדמית',
  'בחודש הבא',
  '2', // אצל הלקוח
  '3', // פרימיום
  '1', // היום הפנוי הראשון
  'יוסי לוי',
  '0501112233',
]);

// 3) חברה גדולה — הבוט לא קובע לבד, מתאם שיחת טלפון.
runScenario('חברה גדולה · תיאום שיחת טלפון', [
  '4', // חברה גדולה
  'חברת הייטק, 40 עובדים, 0533334444',
]);

// 4) מעבר לשיחה אישית באמצע השיחה.
runScenario('מעבר לשיחה אישית עם שרון', [
  '1', // פורטרט
  'דחוף',
  'אשמח לדבר עם שרון בטלפון',
  'רוני, 0547778888',
]);

// 5) צילום צוות — תמחור לפי אדם + חיבור לשרון.
runScenario('צוות עובדים · תמחור לפי אדם', [
  '3', // צוות
  'כן', // לתאם שיחה
  'מיכל, 0521119999',
]);

console.log('\n\n✅ סוף ההדגמה.');
